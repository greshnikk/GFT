DROP DATABASE IF EXISTS GMOGA;
CREATE DATABASE GMOGA;
USE GMOGA;

CREATE TABLE OPERATIONS (ID INT NOT NULL AUTO_INCREMENT,
	OPERATION VARCHAR(2) NOT NULL,
	PRIMARY KEY (ID));
	
CREATE TABLE SEPARATORS (ID INT NOT NULL AUTO_INCREMENT,
	SEPARATORS VARCHAR(2) NOT NULL,
	PRIMARY KEY (ID));
	
CREATE TABLE CONSTANTS (ID INT NOT NULL AUTO_INCREMENT,
	CONSTANT VARCHAR(10) NOT NULL,
	VALUE FLOAT NOT NULL,
	PRIMARY KEY(ID));
	
CREATE TABLE FUNCTIONS (ID INT NOT NULL AUTO_INCREMENT,
	FUNCTION VARCHAR(15) NOT NULL,
	PRIMARY KEY(ID));
	
